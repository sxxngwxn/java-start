package operator;

public class Operator7 {
    public static void main(String[] args) {

        System.out.println(true && true);
        System.out.println(true && false);

        System.out.println(true || false);
        System.out.println(true || false);

        System.out.println(!true);
        System.out.println(!false);

        int a = 15;

        System.out.println(10 < a && a < 20);
    }
}
